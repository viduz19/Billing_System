    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        String total = jTable1.getValueAt(row,3).toString();
        
        Double billval = Double.parseDouble(lbillamou.getText());
        Double totalval = Double.parseDouble(total);
        Double bill_amount = billval - totalval;
        lbillamou.setText(String.valueOf(bill_amount));
        table.removeRow(row);
        JOptionPane.showMessageDialog(this,"Item Removed Successfully!","DONE",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

