    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String item = jComboBox1.getSelectedItem().toString();
        String qty = qtytf.getText();
        String price = pricetf.getText();
        String total = ltotal.getText();
        String bill = lbillamou.getText();
        
         DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
         table.addRow(new Object[] {item,qty,price,total}); 
         
         Double totalval = Double.parseDouble(ltotal.getText());
         Double billval = Double.parseDouble(lbillamou.getText());
         Double bill_amount =totalval + billval;
         lbillamou.setText(String.valueOf(bill_amount));
         
         
         qtytf.setText(null);
         pricetf.setText(null);
         ltotal.setText("0.00");
         JOptionPane.showMessageDialog(this,"Item Add Successfully!","DONE",JOptionPane.INFORMATION_MESSAGE);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

